package OOPS1;
//OOP (Object-Oriented Programming) ek programming paradigm hai jo real-world objects ko programming mein model karta hai. Ismein 4 main concepts hote hain:
//
//Class: Class ek blueprint hota hai jisme aap attributes (properties) aur methods (functions) ko define karte ho. Class se objects create hote hain.
//
//Example: Ek Car class ban sakti hai, jisme properties jaise color, brand, aur speed honge, aur methods jaise drive() aur stop().
//
//Object: Object class ka ek instance hota hai. Har object apne attributes ke sath unique hota hai, lekin wo class ke rules follow karta hai.
//
//Example: Agar Car class hai, toh ek object ho sakta hai "Red Tesla", aur doosra "Blue BMW".
//
//Inheritance: Inheritance ke through ek class doosri class ke properties aur methods ko inherit kar sakti hai. Isse code reuse hota hai.
//
//Example: Agar ek Vehicle class hai, toh Car aur Bike usse inherit karke apni specific cheezein add kar sakte hain, jaise Car mein doors honge aur Bike mein handle type.
//
//Polymorphism: Polymorphism ke through ek function different objects ke liye alag tareeke se behave kar sakta hai.
//
//Example: Agar Animal class mein ek sound() method hai, toh Dog ka object bark karega aur Cat ka object meow karega, even though dono same method call kar rahe hain.
//
//Encapsulation: Is concept mein aap data ko hide karte ho by making attributes private aur unhe methods ke through access karte ho.
//
//Example: Aap speed ko private kar doge aur ek method getSpeed() banake usko access karoge, taki direct changes na kiye ja sakein.
//
//Abstraction: Abstraction ke through aap unnecessary details ko hide karte ho aur sirf essential cheezein dikhate ho.
//
//Example: Ek Phone class mein aapko bas makeCall() method use karna hota hai, iske peeche ka logic ki network kaise connect ho raha hai, wo abstract rahta hai.

